package entity;

/**
 *
 * @author lasin
 */
public class NhanVien {
    private String sdt;
    private String ten;
    private String diachi;

    public NhanVien() {
    }

    public NhanVien(String sdt, String ten, String diachi) {
        this.sdt = sdt;
        this.ten = ten;
        this.diachi = diachi;
    }

    public String getSdt() {
        return sdt;
    }

    public void setSdt(String sdt) {
        this.sdt = sdt;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getDiachi() {
        return diachi;
    }

    public void setDiachi(String diachi) {
        this.diachi = diachi;
    }
    
    
}
